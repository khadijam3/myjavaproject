package Com.Class03;

public class RelationOperator {

	public static void main(String[] args) {
		int a=12;
		int b=11;
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a==b);
		System.out.println(a!=b);
		System.out.println(a>=b);
		System.out.println(a<=b);
		// ration operator only return the value of boolean
	}

}
