package Com.Class02;

public class atmaticOps {

	public static void main(String[] args) {
		int num1=10;
		int num2=20;
		//+ * - % /.
		int sum=num1/num2;
		int sub=num1-num2;
		int mult=num1+num2;
		int div=num1*num2;
		
		System.out.println(sum);
		System.out.println(sub);
		System.out.println(mult);
		System.out.println(div);
		
		double number1=12.50;
		double number2=2.30;
		double sumofdouble=number1+number2;
	
		
		System.out.println(sumofdouble);
		
		
		float b1=13.99f;
		float b2=13.99f;
		float suboffloat=b1-b2;
		float divoffloat=b1/b2;
		float sumoffloat=b1+b2;
		float multoffloat=b1*b2;
		System.out.println( num1-num2);
		System.out.println("the Result of Subtraction of float value is "+ suboffloat);
		System.out.println("the Result of Diveation of float value is "+ divoffloat);
		System.out.println("the Result of Addition of float value is "+ sumoffloat);
		System.out.println("the Result of Multiplication of float value is "+ multoffloat);
		
		int result=5*10-2;
		System.out.println("the Result of Subtraction of float value is "+ result);
		
	}

}
