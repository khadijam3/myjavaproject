package Com.class07;

public class incrementanddecrement {

	public static void main(String[] args) {
		int z=11;
		z=z+1;
		System.out.println(z);
		z+=1;// only add one to variable
		System.out.println(z);
		z++;
		
		int x=20;// subtract one to the variable only used with variable we used with loops
		x-=1;
		x--;
		System.out.println(x);
		//exmple 100++; not working
	}

}
